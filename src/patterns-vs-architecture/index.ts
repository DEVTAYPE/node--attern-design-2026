/* 
PATRONES DE DISEÑO VS ARQUITECTURA DE SOFTWARE

Patrones de diseño:
- Son soluciones reutilizables a problemas comunes en el diseño de software.
- Se centran en aspectos específicos del código y la estructura interna de una aplicación.
- Ejemplos incluyen Singleton, Factory, Observer, Strategy, etc.
- Ayudan a mejorar la mantenibilidad, flexibilidad y escalabilidad del código.

Arquitectura de software:
- Es la estructura general y organización de un sistema de software.
- Define cómo los componentes interactúan entre sí y con el entorno externo.
- Ejemplos incluyen Arquitectura en capas, Microservicios, Arquitectura orientada a servicios (SOA), etc.
- Se enfoca en aspectos como la escalabilidad, rendimiento, seguridad y gestión de datos a nivel del sistema.

En resumen, los patrones de diseño son soluciones específicas para problemas de codificación dentro de una aplicación, mientras que la arquitectura de software es la estructura global que define cómo se organiza y funciona todo el sistema.

APUNTES CLAROS (ARQUITECTURA DE SISTEMAS):

- Siempre hay que tener en cuenta el contexto y entorno.
- La arquitectura de sistemas se basa siempre en los requerimientos del proyecto.

estas cosas puede inflfuir incluso en los stacks de tecnologia.
como tecnologias que soportan el servidor del cliente.

ORDEN: 
ARQUITECTURA DE SISTEMAS -> ARQUITECTURA DE SOFTWARE -> PATRONES DE DISEÑO 

============================
ARQUITECTURA DE SOFTWARE
============================

- Estructura y organización de un sistema de software.
- Define cómo los componentes interactúan entre sí y con el entorno externo.
- Ejemplos: Arquitectura en capas, Microservicios, Arquitectura orientada a servicios (SOA).
- Enfoque en escalabilidad, rendimiento, seguridad y gestión de datos a nivel del sistema.

ANOTES: los mas conocidos, MVC, MVVM, CLEAN ARCHITECTURE,HEXAGONAL.

Las arquitecturas de software son flexibles, adaptables y escalables.

SE PUEDE AGREGAR MAS CAPAS SEGUN SEA NECESARIO, se puede adaptar distintos conceptos de arcquitectura, incluso mezclar varias.

EN RESUMEN ES ORDEN Y PROGRESO.

============================
PATRONES DE DISEÑO
============================

- Soluciones reutilizables a problemas comunes en el diseño de software.
- Se centran en aspectos específicos del código y la estructura interna de una aplicación.
- Ejemplos: Singleton, Factory, Observer, Strategy.
- Mejoran la mantenibilidad, flexibilidad y escalabilidad del código.

APUNTES: Los patrones de diseño son como "plantillas" para resolver problemas específicos en el desarrollo de software.

Los patrones de diseño ayudan a los desarrolladores a comunicarse mejor, ya que proporcionan un vocabulario común para describir soluciones a problemas recurrentes.

A MEDIDA QUE UNO DESARROLLA SALEN PROBLEMAS Y AHI ES DONDE ENTRA LOS PATRONES DE DISEÑO.


*/

