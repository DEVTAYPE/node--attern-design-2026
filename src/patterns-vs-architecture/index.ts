// ARQUITECTURA DE SOFTWARE

/* 
1: DAR ORDEN Y SENTIDO: La arquitectura de software proporciona una estructura clara para el desarrollo del software, definiendo cómo los diferentes componentes interactúan entre sí y con el entorno externo. Esto ayuda a organizar el código de manera lógica y coherente.

2: ESCALABILIDAD Y MANTENIBILIDAD: Una buena arquitectura de software facilita la escalabilidad y mantenibilidad del sistema. Permite agregar nuevas funcionalidades o modificar las existentes sin afectar negativamente al resto del sistema.

3: CUMPLIR CON LOS REQUERIMIENTOS: La arquitectura de software se diseña teniendo en cuenta los requisitos del proyecto, como el rendimiento, la seguridad y la gestión de datos. Esto asegura que el sistema cumpla con las expectativas y necesidades del usuario final.

4: LAS HERRAMIENTAS SE ELIGEN EN FUNCION DEL REQUERIMINETO Y NO AL REVES: Las tecnologías y herramientas utilizadas en el desarrollo del software deben seleccionarse en función de los requisitos del proyecto y no al revés. Esto garantiza que la solución técnica sea adecuada para resolver los problemas específicos del sistema.

5: SON FLEXIBLES Y ADAPTABLES: Las arquitecturas de software son flexibles y adaptables, lo que permite ajustarlas según las necesidades cambiantes del proyecto o del entorno tecnológico. Esto facilita la evolución del sistema a lo largo del tiempo.

*/

